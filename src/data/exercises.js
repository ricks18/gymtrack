export const getExercisesByType = (type) => {
  return exerciseDatabase.filter(exercise => exercise.type === type);
};

export const getAllTypes = () => {
  return [...new Set(exerciseDatabase.map(exercise => exercise.type))];
};

export const searchExercises = (query) => {
  const searchTerm = query.toLowerCase();
  return exerciseDatabase.filter(exercise => 
    exercise.name.toLowerCase().includes(searchTerm) ||
    exercise.type.toLowerCase().includes(searchTerm) ||
    exercise.muscle.toLowerCase().includes(searchTerm)
  );
};
